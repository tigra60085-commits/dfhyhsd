async def case_format_input_message(update: Update, context: ContextTypes.DEFAULT_TYPE) -> int:
    text = update.message.text.strip()

    if len(text) < 30:
        await update.message.reply_text(
            "‚ö†Ô∏è –¢–µ–∫—Å—Ç —Å–ª–∏—à–∫–æ–º –∫–æ—Ä–æ—Ç–∫–∏–π. –í–≤–µ–¥–∏—Ç–µ –ø–æ–ª–Ω–æ–µ –æ–ø–∏—Å–∞–Ω–∏–µ –∫–ª–∏–Ω–∏—á–µ—Å–∫–æ–≥–æ —Å–ª—É—á–∞—è.",
        )
        return CASE_FORMAT_INPUT

    context.user_data["cf_raw"] = text
    await update.message.reply_text(
        f"‚úÖ –¢–µ–∫—Å—Ç –ø–æ–ª—É—á–µ–Ω ({len(text)} —Å–∏–º–≤–æ–ª–æ–≤).\n\n"
        "–í—ã–±–µ—Ä–∏—Ç–µ *—Ñ–æ–∫—É—Å* —Ä–∞–∑–±–æ—Ä–∞ –∫–µ–π—Å–∞:",
        parse_mode="Markdown",
        reply_markup=case_format_focus_keyboard(),
    )
    return CASE_FORMAT_FOCUS


async def case_format_focus_callback(update: Update, context: ContextTypes.DEFAULT_TYPE) -> int:
    query = update.callback_query
    await query.answer()
    data = query.data

    if data == "back:main":
        await query.message.reply_text("–ì–ª–∞–≤–Ω–æ–µ –º–µ–Ω—é:", reply_markup=main_menu_keyboard())
        await query.message.delete()
        return MAIN_MENU

    if data.startswith("cff:"):
        context.user_data["cf_focus"] = data[len("cff:"):]
        focus_label = _FOCUS_LABELS.get(context.user_data["cf_focus"], "–û–±—â–∏–π —Ä–∞–∑–±–æ—Ä")
        await query.edit_message_text(
            f"–§–æ–∫—É—Å: *{focus_label}*\n\n"
            "–í—ã–±–µ—Ä–∏—Ç–µ *–∞—É–¥–∏—Ç–æ—Ä–∏—é* –∏ –Ω–∞—Å—Ç—Ä–æ–π–∫—É –∞–Ω–æ–Ω–∏–º–∏–∑–∞—Ü–∏–∏:",
            parse_mode="Markdown",
            reply_markup=case_format_options_keyboard(),
        )
        return CASE_FORMAT_OPTIONS

    return CASE_FORMAT_FOCUS


async def case_format_options_callback(update: Update, context: ContextTypes.DEFAULT_TYPE) -> int:
    query = update.callback_query
    await query.answer()
    data = query.data

    if data == "back:main":
        await query.message.reply_text("–ì–ª–∞–≤–Ω–æ–µ –º–µ–Ω—é:", reply_markup=main_menu_keyboard())
        await query.message.delete()
        return MAIN_MENU

    if data == "cf:again":
        await query.message.reply_text(
            "–í—Å—Ç–∞–≤—å—Ç–µ —Ç–µ–∫—Å—Ç –Ω–æ–≤–æ–≥–æ –∫–ª–∏–Ω–∏—á–µ—Å–∫–æ–≥–æ —Å–ª—É—á–∞—è:",
            reply_markup=back_keyboard("back:main"),
        )
        return CASE_FORMAT_INPUT

    if not data.startswith("cfopt:"):
        return CASE_FORMAT_OPTIONS

    # cfopt:<audience>:<anonymize>
    parts = data[len("cfopt:"):].split(":")
    audience = parts[0] if len(parts) > 0 else "resident"
    anonymize = parts[1] == "1" if len(parts) > 1 else True

    context.user_data["cf_audience"] = audience
    context.user_data["cf_anonymize"] = anonymize

    raw = context.user_data.get("cf_raw", "")
    focus = context.user_data.get("cf_focus", "general")

    await query.edit_message_text("‚è≥ –§–æ—Ä–º–∞—Ç–∏—Ä—É—é –∫–µ–π—Å...")

    # Apply anonymization
    processed = _anonymize(raw) if anonymize else raw

    # Build Telegram preview
    tg_text = _build_telegram_text(processed, focus, audience)
    for i in range(0, len(tg_text), 4000):
        await query.message.reply_text(tg_text[i:i + 4000], parse_mode="Markdown")

    # Build and send docx
    try:
        docx_buf = _build_docx(processed, raw, focus, audience, anonymize)
        slug = f"case_{date.today().strftime('%Y%m%d')}"
        filename = f"{slug}_formatted.docx"
        await query.message.reply_document(
            document=InputFile(docx_buf, filename=filename),
            caption="üìÑ –ü–æ–ª–Ω—ã–π —Ä–∞–∑–±–æ—Ä –∫–µ–π—Å–∞ (.docx)",
        )
    except ImportError:
        await query.message.reply_text(
            "‚ö†Ô∏è python-docx –Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω. –£—Å—Ç–∞–Ω–æ–≤–∏—Ç–µ: `pip install python-docx`",
            parse_mode="Markdown",
        )
    except Exception as exc:
        await query.message.reply_text(f"‚ö†Ô∏è –û—à–∏–±–∫–∞ –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏ .docx: {exc}")

    await query.message.reply_text(
        "–ö–µ–π—Å –æ—Ç—Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω.",
        reply_markup=case_format_result_keyboard(),
    )
    return CASE_FORMAT_OPTIONS


# ‚îÄ‚îÄ‚îÄ Anonymization ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

_NAME_PATTERN = re.compile(
    r'\b([–ê-–Ø–Å][–∞-—è—ë]+(?:\s+[–ê-–Ø–Å][–∞-—è—ë]+){1,2})\b'
)
_DATE_PATTERN = re.compile(
    r'\b(\d{1,2}[./]\d{1,2}[./]\d{2,4}|\d{4}\s*–≥\.?)\b'
)
_AGE_PATTERN = re.compile(
    r'\b(\d{1,3})\s*(–ª–µ—Ç|–≥–æ–¥–∞?|–≥–æ–¥)\b'
)

_name_counter = [0]
_name_map: dict = {}


def _anonymize(text: str) -> str:
    global _name_map
    _name_map = {}
    _name_counter[0] = 0

    def replace_name(m: re.Match) -> str:
        name = m.group(1)
        if name not in _name_map:
            _name_counter[0] += 1
            _name_map[name] = f"–ü–∞—Ü–∏–µ–Ω—Ç {'–ê –ë –í –ì –î –ï'.split()[min(_name_counter[0]-1, 5)]}"
        return _name_map[name]

    text = _NAME_PATTERN.sub(replace_name, text)
    text = _DATE_PATTERN.sub(lambda m: "[–¥–∞—Ç–∞]", text)
    # Round ages to nearest 5
    def round_age(m: re.Match) -> str:
        try:
            age = int(m.group(1))
            rounded = round(age / 5) * 5
            return f"~{rounded} {m.group(2)}"
        except ValueError:
            return m.group(0)
    text = _AGE_PATTERN.sub(round_age, text)
    return text


